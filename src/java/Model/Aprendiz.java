public class Aprendiz {
    int id;
    int doca, tela;
    String noma, apea, emaa;

    public Aprendiz() {
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getDoca() {
        return doca;
    }

    public void setDoca(int doca) {
        this.doca = doca;
    }

    public int getTela() {
        return tela;
    }

    public void setTela(int tela) {
        this.tela = tela;
    }

    public String getNoma() {
        return noma;
    }

    public void setNoma(String noma) {
        this.noma = noma;
    }

    public String getApea() {
        return apea;
    }

    public void setApea(String apea) {
        this.apea = apea;
    }

    public String getEmaa() {
        return emaa;
    }

    public void setEmaa(String emaa) {
        this.emaa = emaa;
    }
    
    
}

 